<?php

namespace Cerbero\LaravelEnum;

/**
 * The enum definition.
 *
 */
class EnumDefinition
{
    /**
     * The name of the constant.
     *
     * @var string
     */
    public $name;

    /**
     * The key of the enum.
     *
     * @var mixed
     */
    public $key;

    /**
     * The value of the enum.
     *
     * @var mixed
     */
    public $value;
}
